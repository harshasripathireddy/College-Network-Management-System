<p>
CCTV stands for closed-circuit television and is commonly known as video surveillance. “Closed-circuit” means broadcasts are usually transmitted to a limited (closed) number of monitors, unlike “regular” TV, which is broadcast to the public at large. CCTV networks are commonly used to detect and deter criminal activities, and record traffic infractions, but they have other uses.CCTV technology was first developed in 1942 by German scientists to monitor the launch of V2 rockets. It was later used by American scientists during the testing of the atomic bomb.</p>
<h2>HOW DOES CCTV WORK?</h2>
<p>Analog and digital systems work quite differently but modern CCTV networks use conversion software and hardware to convert analog to digital. This process is called retrofitting.
A traditional CCTV system comprises:
One or more cameras (analog or digital), each with a lens equipped with an image sensor.
A recorder – Either a standard video tape recorder for analog systems, or a Direct Video Recorder (DVR) or Network Video Recorder (NVR) for digital systems.
Cables – Either RJ45 for digital or coaxial for analog.
One or more monitors to which the images are transmitted.</p>
