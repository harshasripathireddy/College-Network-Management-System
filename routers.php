<p><center><h2 style="color:DodgerBlue">ROUTERS<h2></center></p><hr>
<p>A router is a layer 3 or network layer device.It connects different networks together and sends data packets from one network to another.
A router can be used both in LANs (Local Area Networks) and WANs (Wide Area Networks).It transfers data in the form of IP packets.In order to transmit data, it uses IP address mentioned in the destination field of the IP packet.
Routers have a routing table in it that is refreshed periodically according to the changes in the network.In order to transmit data packets, it consults the table and uses a routing protocol.
In order to prepare or refresh the routing table, routers share information among each other.Routers provide protection against broadcast storms.
Routers are more expensive than other networking devices like hubs,bridges and switches.</p>

<center><h3 style="color:DodgerBlue">Types of Routers:</h3></center><hr>
A variety of routers are available depending upon their usages. The main types of routers are
<p>Wireless Router − They provide WiFi connection WiFi devices like laptops, smartphones etc. They can also provide standard Ethernet routing. For indoor connections, the range is 150 feet while its 300 feet for outdoor connections.</p>
<img src="r1.jpg" align=right width="300" height="250">
<p>Broadband Routers − They are used to connect to the Internet through telephone and to use voice over Internet Protocol (VoIP) technology for providing high-speed Internet access. They are configured and provided by the Internet Service Provider (ISP).</p>
<p>Core Routers − They can route data packets within a given network, but cannot route the packets between the networks. They helps to link all devices within a network thus forming the backbone of network. It is used by ISP and communication interfaces.</p>
<p>Edge Routers − They are low-capacity routers placed at the periphery of the networks. They connect the internal network to the external networks, and are suitable for transferring data packets across networks. They use Border Gateway Protocol (BGP) for connectivity. There are two types of edge routers, subscriber edge routers and label edge routers.</p>
<p>Brouters − Brouters are specialised routers that can provide the functionalities of bridges as well. Like a bridge, brouters help to transfer data between networks. And like a router, they route the data within the devices of a network.
</p>
