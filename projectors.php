<center><h2 style="color:DodgerBlue">PROJECTORS</h2></center><hr>
<img src="proo.png" align=right width="300" height="200">
<p>A projector or image projector is an optical device that projects an image (or moving images) onto a surface, commonly a projection screen. Most 
projectors create an image by shining a light through a small transparent lens, but some newer types of projectors can project the image directly, by using 
lasers. A virtual retinal display, or retinal projector, is a projector that projects an image directly on the retina instead of using an external projection 
screen
The most common type of projector used today is called a video projector. Video projectors are digital replacements for earlier types of projectors such as 
slide projectors and overhead projectors. These earlier types of projectors were mostly replaced with digital video projectors throughout the 1990s and 
early 2000s,[1] but old analog projectors are still used at some places. The newest types of projectors are handheld projectors that use lasers or LED3s to 
project images
Movie theaters used a type of projector called a movie projector, nowadays mostly replaced with digital cinema video projectors.</p>

<center><h2 style="color:DodgerBlue">DIFFERENT PROJECTOR TYPES</h2></center><hr>
<p>Projectors can be roughly divided into three categories, based on the type of input. Some of the listed projectors were capable of projecting 
several types of input. For instance: video projectors were basically developed for the projection of prerecorded moving images, but are regularly used 
for still images in PowerPoint presentations and can easily be connected to a video camera for real-time input. The magic lantern is best known for the 
projection of still images, but was capable of projecting moving images from mechanical slides since its invention and was probably at its peak of popularity 
when used in phantasmagoria shows to project moving images of ghosts.</p>
<p>A video projector, also known as a digital projector, may project onto a traditional reflective projection screen, or it may be built into a cabinet with a translucent rear-projection screen to form a single unified display device.

Common display resolutions include SVGA (800×600 pixels), XGA (1024×768 pixels), SXGA+ (1400x1050 pixels), 720p (1280×720 pixels), and 1080p (1920×1080 pixels), 4K UHD (3840x2160), as well as 16:10 aspect ratio resolutions including WXGA+ (1280x800 pixels) and WUXGA (1920x1200 pixels)

The cost of a projector is typically driven by its base technology, features, resolution, and light output. A projector with a higher light output (measured in lumens, “lm”) is required for a larger screen or for a room with a larger amount of ambient light.[2] For example, a light output of approximately 1500 to 2500 ANSI lumens is suitable for small screens viewed in rooms with low ambient light; approximately 2500 to 4000 lm is suitable for medium-sized screens with some ambient light; over 4000 lm is needed for very large screens or for use in rooms with no lighting control such as conference rooms. High brightness large-venue models are increasingly common in Boardrooms, Auditoriums and other high profile spaces, and models up to 30,000 lm are used in large staging applications such as concerts, keynote addresses and displays projected on buildings.

A few camcorders have a built-in projector suitable to make a small projection; a few more powerful "pico projectors" are pocket-sized, and many projectors are portable.

