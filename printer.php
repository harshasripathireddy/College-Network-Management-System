<p><center><h2 style="color:DodgerBlue;">PRINTERS</h2></center></p><hr>

 printers work by converting digital images and text into physical copies. They do this using a driver or specialised software that has been designed 
to convert the file into a language that the printer can understand.The image or text is then recreated on to the page using a series of miniscule dots.
<img src="history.jpg" width="300" height="200" style="float:right">
<h3>Printing Principle</h3>
Pressurized ink is supplied to the print head from the controller. Ink is fed to the nozzle which has the piezoelectric oscillator and discharge hole.
 The ink is discharged while being oscillated by the piezoelectric oscillator, and it is simultaneously given a negative electrostatic charge.
<h3>Types of Printers:</h3><ol>

<li><h4>Laser Printer</h4></li>
<p>Laser printers make use of light or a focused beam for transferring the text and images on the paper. Inside the laser printer, a cylindrical drum is present known as Photoreceptor on which laser beams are fired. This is done as the paper passes through the printer. The laser printing machine prints images and texts on the photoreceptor. One can use a laser printer for getting instant print during emergencies.</p>

<li><h4>Photo Printer</h4></li>
<p>Photo printing machines are basically an inkjet printer which is designed for printing on photo papers which are extremely white. These photo papers used in Photo printers are coated with a coating which ensures that the ink used for printing the photo is not smeared off. Photo printing machines are able to print photos of all sizes. Though some are used specifically for printing only wallet-sized photos of 3x5" or 4x6".

</p>

<li><h4>Color Printer</h4></li>
Color printers are those printers which are able to print more than one/single color. Many of these machines work on the CMYK color-model. In CMYK color model, 4 basic colors i.e the Cyan, Black, Yellow and Magenta. The CMYK model prints all these basic colors close to each other and then stimulates most of the other colors (except fluorescent yellow color). Further, some low-rated and low-priced color printers use only 3 basic colors - yellow, cyan and magenta. Such machines will not be able to print the true black color and also the colors will tend to look faded.
<li><h4>Inkjet Printer</h4></li>
<p>Inkjet Printing machines are the regularly used printers for the computers. Inkjet Printers use a special type of ink for printing on the paper. Hence, Inkjet Printers are particularly used to get high quality color prints. They are also capable of giving instant print outputs. These machines are preferred by those people who need prints once in a while and not on a daily basis.

</p>

<li><h4>Dot Matrix Printer</h4></li>
<p>Dot Matrix printers are also known as Impact Matrix Printing. Dot Matrix printers use a printing process in which the ink will be applied on the surface of the paper/layout. The pins in the Dot Matrix printer strike a ribbon which is coated by ink and force the contact in between the ribbon and the paper. This creates a small dot on the paper and the final dot matrix image is created when these individual dots are combined.

</p>
<img src="prntrs.jpg" alt="prntrs">
<li><h4>Digital Printer</h4></li>
<p>In digital printers, the electronic file or the digital-based image is directly printed on the material. The image which has to be printed is directly sent to the digital printer via a digital file format such as TIFF, PDF or any other formats. In digital printers, there is not at all any need of the printing plates which are required in offset printers. These machines can directly print on photo paper, paper, fabric, canvas, cardstock or on any given subtract.

</p>
<li><h4>Thermal Printers</h4></li>
<p>Thermal printers use heat for producing the image on the paper. Thermal printers have become more popular due to their speed, advanced tech and high quality print. Thermal printers don't use ink or toners, rather they largely depend on the thermal papers for printing the images. Thermal printers are very easy to use as they have less buttons and use softwares for the printing process. Also, thermal printers are less expensive and come in different sizes and models which makes them easily affordable by most of the consumers.

</p>
<li><h4>Ink Tank Printer</h4></li>
<p>In ink tank printers, the ink tank feeds the liquid ink into the separate printhead which is inside the printer. The printhead distributes this ink in droplets form on the surface of the page which forms the images or text. Ink tank printers have a large capacity for storing ink. Due to this, you will be able to refill the ink tank which is a lot cheaper rather than changing the whole cartridge of ink.

</p>
<img src="prntr.jpg" alt"prntr" width="300" height="200" style="float:right">
<li><h4>Laserjet Printer</h4></li>
<p>Laserjet printers use its laser beams for printing your print on a metal-cylinder known as a drum. This drum in the laserjet printer, attracts the powdered toner which is present in the cartridge using the static electricity. Then, the toner is rolled onto the paper by the drum which is in the form of your print. Further, as the toner passes underneath, it is melted on the paper using the heat from a fuser. And finally, the print comes out from the Laserjet printer.

</p>
<li><h4>Barcode Printer</h4></li>
<p>Barcode Printers are those printers which are designed for producing the tags or the barcode labels which can be either attached to or even directly printed on the objects. Barcode Printers make use of the thermal transfer printers or direct thermal printers for creating labels. Of these 2 methods, the direct thermal method costs less but the labels produced using this method may become illegible when they are exposed to direct sunlight, chemical vapors or heat.

</p>
<li><h4>PVC Card Printer</h4></li>
<p>PVC Cards/ Plastic Cards have a very smooth surface due to which the traditional inkjet or laser printers cannot be used for printing on them. Due to this reason, the PVC Card Printers are used to print on such smooth surfaces. In PVC Card Printers, the heater printhead is run over the ribbon. This process helps in transferring the colors onto the card which makes your design print on it.
</p>

