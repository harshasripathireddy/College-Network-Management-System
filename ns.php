<center><font style="color:DodgerBlue"><h2>NETWORK SWITCHES</h2></font></center><hr>
<img src="nsp.jpg" height="400" width="250" align="right">

<p>Switch is a device which divides a network into different subnetworks called subject. It prevents traffic overloading in a network. Many computer in a network can be connected to a switch that to receive and forward data to the destination device.

A network switch is a multiport network bridge that uses MAC addresses to forward data at the data link layer (layer 2) of the OSI model. Some switches can also forward data at the network layer (layer 3) by additionally incorporating routing functionality. Switch switches are commonly known as layer-3 switches or multilayer switches.<br><br>A switch is a device in a computer network that connects other devices together. Multiple data cables are plugged into a switch to enable communication between different networked devices. Switches manage the flow of data across a network by transmitting a received network packet only to the one or more devices for which the packet is intended. Each networked device connected to a switch can be identified by its network address, allowing the switch to direct the flow of traffic maximizing the security and efficiency of the network.</p>
 <h4>Types of Switches</h4>
<h5>Modular switches vs. fixed-configuration switches</h5>
<p>There are two main categories of switches: modular and fixed configuration. There are variations among these categories of network switches, but the primary definition of each remains the same. </p>
<h5>Modular switches</h5>
<p>Modular switches let you add expansion modules as needed, giving you flexibility as network requirements change. Expansion modules are application-specific and include those for firewalls, wireless connectivity, or network analysis. They may also allow for additional interfaces, power supplies, or cooling fans. This type of switch provides you with the most flexibility, but at a higher cost. </p>
<h5>Fixed-configuration switches</h5>
<p>Fixed-configuration switches provide a fixed number of ports and are typically not expandable, which makes them less expensive overall. Fixed-configuration switches include unmanaged switches, smart switches, and managed switches.</p>
<img src="ns1.jpg" align="right" height="100" width="300">
<h5>Unmanaged switches</h5>
<p>Unmanaged switches are typically used to provide basic connectivity. They're designed to be plug and play; no configuration is needed. Unmanaged switches are most effective when only basic switching and connectivity are required. You will often see them in home networks or wherever only a few ports are needed, such as at a desk, in a lab, or in a conference room. 

Some unmanaged switches do offer limited advanced capabilities, however—as the name implies—these switches generally cannot be modified or managed. </p>
<img src="ns2.jpg" align="right" height="100" width="300">
<h5>Smart switches</h5>
<p>Smart switches offer some management and segmentation, quality of service, and security capabilities, so they can be a cost-effective alternative to modular switches. Still, they are not as scalable as managed switches. These switches are typically deployed at the edge of a large network (while managed switches are used in the core), as the infrastructure for smaller networks, or for low complexity networks. </p>
<img src="ns3.jpg" align="right" height="100" width="300">
<h5>Managed switches</h5>
<p>Among fixed-configuration switches, managed switches are designed to deliver the most comprehensive set of features to provide the best application experience, the highest levels of security, the most precise control and management of the network, and the greatest scalability. As a result, managed switches are usually deployed as aggregation/access switches in very large networks or as core switches in smaller networks. 

Managed switches are the most expensive option of fixed-configuration switches and are most common in organizations with large- or growing- networks.</p>
<center><font style="color:DodgerBlue"><h2>HUB</h2></font></center><hr>
<img src="ns4.jpg" align="right" height="300" width="300">
<p>Hubs are networking devices operating at a physical layer of the OSI model that are used to connect multiple devices in a network. They are generally used to connect computers in a LAN.

<br><br>A hub has many ports in it. A computer which intends to be connected to the network is plugged in to one of these ports. When a data frame arrives at a port, it is broadcast to every other port, without considering whether it is destined for a particular destination device or not.</p>

<h4>Types of Hubs</h4>
<h5>Passive Hubs</h5>
<p> Passive hubs connects nodes in a star configuration by collecting wiring from nodes. They broadcast signals onto the network without amplifying or regenerating them. As they cannot extend the distance between nodes, they limit the size of the LAN.</p>
<h5>Active Hubs</h5>
<p> Active hubs amplify and regenerate the incoming electrical signals before broadcasting them. They have their own power supply and serves both as a repeater as well as connecting centre. Due to their regenerating capabilities, they can extend the maximum distance between nodes, thus increasing the size of LAN.</p>
<h5>Intelligent Hubs</h5>
<p>Intelligent hubs are active hubs that provide additional network management facilities. They can perform a variety of functions of more intelligent network devices like network management, switching, providing flexible data rates etc.</p>
